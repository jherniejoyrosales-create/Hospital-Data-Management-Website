// Helper function to calculate days since date
const calculateDaysSince = (dateString: string): number => {
  const date = new Date(dateString);
  const today = new Date();
  const diffTime = Math.abs(today.getTime() - date.getTime());
  const diffDays = Math.ceil(diffTime / (1000 * 60 * 60 * 24));
  return diffDays;
};

// Helper function to parse date from various formats
const parseDate = (dateStr: string): string => {
  if (!dateStr || dateStr.trim() === '') return '';
  
  // Handle formats like "12/27/2025", "February 3, 2026", "01/07/2026"
  const date = new Date(dateStr);
  if (!isNaN(date.getTime())) {
    return date.toISOString().split('T')[0];
  }
  return '';
};

// Helper function to parse confinement period
const parseConfinementPeriod = (period: string): { start: string; end: string } => {
  if (!period || period.trim() === '') {
    return { start: '2026-01-01', end: '2026-01-01' };
  }
  
  const parts = period.split('-').map(p => p.trim());
  if (parts.length === 2) {
    const start = parseDate(parts[0]);
    const end = parseDate(parts[1]);
    return { start: start || '2026-01-01', end: end || '2026-01-01' };
  }
  
  return { start: '2026-01-01', end: '2026-01-01' };
};

// Helper function to parse name
const parseName = (fullName: string): { lastName: string; firstName: string; middleName: string } => {
  if (!fullName) return { lastName: '', firstName: '', middleName: '' };
  
  const parts = fullName.split(',').map(p => p.trim());
  if (parts.length >= 2) {
    const lastName = parts[0];
    const firstMiddle = parts[1].split(' ');
    const firstName = firstMiddle[0] || '';
    const middleName = firstMiddle.slice(1).join(' ') || '';
    return { lastName, firstName, middleName };
  }
  
  return { lastName: fullName, firstName: '', middleName: '' };
};

// Helper function to remove commas from numbers
const parseNumber = (numStr: string): number => {
  if (!numStr || numStr.trim() === '') return 0;
  return parseFloat(numStr.replace(/,/g, ''));
};

